/**
 * Generate the Ethereum half of `@opaquecash/deployments` from the canonical
 * machine-readable records in `infra/deployments/*.json` plus the compiled
 * Hardhat artifacts (ABIs).
 *
 *   npm run generate            # writes into ../sdk/packages/deployments
 *   OPAQUE_SDK_DIR=/path/sdk npm run generate
 *
 * Run after every deploy that changes an address, then rebuild the SDK.
 * Requires `npx hardhat compile` to have produced `artifacts/`.
 */
import { readdirSync, readFileSync, writeFileSync, existsSync, mkdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const INFRA = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SDK_DIR = resolve(
  process.env.OPAQUE_SDK_DIR ?? join(INFRA, "..", "..", "sdk"),
);
const OUT_DIR = join(SDK_DIR, "packages", "deployments", "src", "generated");
const DEPLOYMENTS_DIR = join(INFRA, "deployments");
const ARTIFACTS_DIR = join(INFRA, "artifacts", "contracts");

/** Contracts whose ABIs ship with @opaquecash/deployments (exported camelCase). */
const ABI_CONTRACTS = [
  "StealthMetaAddressRegistry",
  "StealthAddressAnnouncer",
  "OpaqueSchemaRegistry",
  "OpaqueAttestationRegistry",
  "OpaqueReputationVerifierV2",
  "UABSender",
  "UABReceiver",
  "RelayerRegistry",
  "OpaqueDisclosureRegistry",
];

const HEADER = (script) =>
  `// AUTO-GENERATED by ethereum/infra/scripts/${script} — do not edit.\n` +
  `// Source of truth: ethereum/infra/deployments/*.json (+ hardhat artifacts for ABIs).\n`;

function lowerFirst(s) {
  return s.charAt(0).toLowerCase() + s.slice(1);
}

function loadRecords() {
  return readdirSync(DEPLOYMENTS_DIR)
    .filter((f) => f.endsWith(".json"))
    .map((f) => JSON.parse(readFileSync(join(DEPLOYMENTS_DIR, f), "utf8")))
    .sort((a, b) => a.chainId - b.chainId);
}

function emitEthereum(records) {
  const entries = records
    .map((r) => {
      const c = r.contracts;
      const tokens = r.tokens
        .map(
          (t) =>
            `      { address: "${t.address}", symbol: "${t.symbol}", decimals: ${t.decimals}${t.native ? ", native: true" : ""} },`,
        )
        .join("\n");
      return `  ${r.chainId}: {
    chainId: ${r.chainId},
    name: "${r.name}",
    contracts: {
      stealthMetaAddressRegistry: "${c.StealthMetaAddressRegistry}",
      stealthAddressAnnouncer: "${c.StealthAddressAnnouncer}",
      opaqueSchemaRegistry: "${c.OpaqueSchemaRegistry}",
      opaqueAttestationRegistry: "${c.OpaqueAttestationRegistry}",
      opaqueReputationVerifierV2: "${c.OpaqueReputationVerifierV2}",
      groth16VerifierV2: "${c.Groth16VerifierV2}",
      uabSender: "${c.UABSender}",
      uabReceiver: "${c.UABReceiver}",
      relayerRegistry: "${c.RelayerRegistry}",
      opaquePrivacyPool: "${c.OpaquePrivacyPool}",
      withdrawalVerifier: "${c.WithdrawalVerifier}",
      opaqueDisclosureRegistry: "${c.OpaqueDisclosureRegistry}",
      disclosureVerifier: "${c.DisclosureVerifier}",
      wormholeCore: "${c.WormholeCore}",
    },
    wormhole: { chainId: ${r.wormhole.chainId}, sourceChainId: ${r.wormhole.sourceChainId} },
    psrFromBlock: ${r.psrFromBlock}n,
    uabFromBlock: ${r.uabFromBlock}n,
    tokens: [
${tokens}
    ],
  },`;
    })
    .join("\n");

  return `${HEADER("generate-deployments.mjs")}import type { EvmDeployment } from "../types.js";

/** Opaque EVM deployments keyed by chain id. */
export const EVM_DEPLOYMENTS: Record<number, EvmDeployment> = {
${entries}
};
`;
}

function emitAbis() {
  let out = `${HEADER("generate-deployments.mjs")}`;
  for (const name of ABI_CONTRACTS) {
    const artifact = join(ARTIFACTS_DIR, `${name}.sol`, `${name}.json`);
    if (!existsSync(artifact)) {
      throw new Error(`Missing artifact ${artifact} — run \`npx hardhat compile\` first.`);
    }
    const { abi } = JSON.parse(readFileSync(artifact, "utf8"));
    out += `\n/** ABI of \`${name}\` (from hardhat artifacts). */\nexport const ${lowerFirst(name)}Abi = ${JSON.stringify(abi, null, 2)} as const;\n`;
  }
  return out;
}

const records = loadRecords();
mkdirSync(OUT_DIR, { recursive: true });
writeFileSync(join(OUT_DIR, "ethereum.ts"), emitEthereum(records));
writeFileSync(join(OUT_DIR, "abis.ts"), emitAbis());
console.log(
  `Wrote ${records.length} EVM deployment(s) + ${ABI_CONTRACTS.length} ABI(s) to ${OUT_DIR}`,
);
